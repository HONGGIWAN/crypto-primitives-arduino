static inline uint32_t rot32r8(uint32_t value)
{
    return (value >> 8) ^ (value << 24);
}

static uint32_t sub_word(uint32_t value)
{
    uint8_t* ptr = (uint8_t*) &value;
    
    ptr[0] = SBOX[ptr[0]];
    ptr[1] = SBOX[ptr[1]];
    ptr[2] = SBOX[ptr[2]];
    ptr[3] = SBOX[ptr[3]];

    return value;
}

static void add_round_keys(uint8_t* block, const uint8_t* rks) {
   block[0] ^= rks[0];
   block[1] ^= rks[4];
   block[2] ^= rks[8];
   block[3] ^= rks[12];

   block[4] ^= rks[1];
   block[5] ^= rks[5];
   block[6] ^= rks[9];
   block[7] ^= rks[13];

   block[8] ^= rks[2];
   block[9] ^= rks[6];
   block[10] ^= rks[10];
   block[11] ^= rks[14];

   block[12] ^= rks[3];
   block[13] ^= rks[7];
   block[14] ^= rks[11];
   block[15] ^= rks[15];
}

static void swap(uint8_t* block, size_t pos1, size_t pos2)
{
    uint8_t tmp = block[pos1];
    block[pos1] = block[pos2];
    block[pos2] = tmp;
}

static void transpose(uint8_t* block)
{
    swap(block, 1, 4);
    swap(block, 2, 8);
    swap(block, 3, 12);
    swap(block, 6, 9);
    swap(block, 7, 13);
    swap(block, 11, 14);
}

static void sub_bytes(uint8_t* block)
{
    for (int i = 0; i < 16; ++i) {
        block[i] = SBOX[block[i]];
    }
}

static void shift_rows(uint8_t* block)
{
    uint8_t tmp = block[4];
    block[4] = block[5];
    block[5] = block[6];
    block[6] = block[7];
    block[7] = tmp;

    swap(block, 8, 10);
    swap(block, 9, 11);

    tmp = block[15];
    block[15] = block[14];
    block[14] = block[13];
    block[13] = block[12];
    block[12] = tmp;
}

static void inv_shift_rows(uint8_t* block)
{
    uint8_t tmp = block[7];
    block[7] = block[6];
    block[6] = block[5];
    block[5] = block[4];
    block[4] = tmp;

    swap(block, 8, 10);
    swap(block, 9, 11);

    tmp = block[12];
    block[12] = block[13];
    block[13] = block[14];
    block[14] = block[15];
    block[15] = tmp;
}

static void mix_columns(uint8_t* in)
{
    uint8_t block[16] = {0};

    for (int i = 0; i < 4; ++i) {
        block[i     ] = GFMUL_2[in[i]] ^ GFMUL_3[in[i + 4]] ^         in[i + 8]  ^         in[i + 12];
        block[i +  4] =         in[i]  ^ GFMUL_2[in[i + 4]] ^ GFMUL_3[in[i + 8]] ^         in[i + 12];
        block[i +  8] =         in[i]  ^         in[i + 4]  ^ GFMUL_2[in[i + 8]] ^ GFMUL_3[in[i + 12]];
        block[i + 12] = GFMUL_3[in[i]] ^         in[i + 4]  ^         in[i + 8]  ^ GFMUL_2[in[i + 12]];
    }

    memcpy(in, block, 16);
}

static void aes_encrypt(uint8_t* ct, const uint8_t* pt, const uint8_t* rks, size_t rounds)
{
    uint8_t block[16] = {0};
    memcpy(block, pt, 16);
    transpose(block);

    add_round_keys(block, rks);
    rks += 16;

    for (int i = 0; i < rounds - 1; ++i, rks += 16)
    {
        sub_bytes(block);
        shift_rows(block);
        mix_columns(block);
        add_round_keys(block, rks);
    }

    sub_bytes(block);
    shift_rows(block);
    add_round_keys(block, rks);

    transpose(block);
    memcpy(ct, block, 16);
}

void aes128_keygen(uint8_t* rks, const uint8_t* mk)
{
    const uint32_t* key = (const uint32_t*) mk;
    uint32_t* rk = (uint32_t*) rks;

    memcpy(rk, key, 16);

    for (int i = 0; i < 10; ++i) {
        rk[4] = rk[0] ^ sub_word(rot32r8(rk[3])) ^ RC[i];
        rk[5] = rk[1] ^ rk[4];
        rk[6] = rk[2] ^ rk[5];
        rk[7] = rk[3] ^ rk[6];
        rk += 4;
    }
}

void aes128_encrypt(uint8_t* ct, const uint8_t* pt, const uint8_t* rks)
{
    aes_encrypt(ct, pt, rks, AES128_ROUNDS);
}
